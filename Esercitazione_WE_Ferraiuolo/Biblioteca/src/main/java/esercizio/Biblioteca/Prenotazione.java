package esercizio.Biblioteca;

public class Prenotazione {
	
	int idPrenotazione;
	String nome;
	String cognome;
	String titolo;
	
	// Metodo costruttore
	public Prenotazione(int idPrenotazione, String nome, String cognome, String titolo) {
		this.idPrenotazione = idPrenotazione;
		this.nome = nome;
		this.cognome = cognome;
		this.titolo = titolo;
	}
	
	

}
