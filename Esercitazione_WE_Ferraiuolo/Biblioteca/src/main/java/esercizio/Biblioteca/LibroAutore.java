package esercizio.Biblioteca;

public class LibroAutore {
	
	// Attributi della classe Libro
	int idLibro;
	String titolo;
	int numCopie;
	String nomeAutore;
	String cognomeAutore;
	
	// Metodo costruttore
	public LibroAutore(int idLibro, String titolo, int numCopie, String nomeAutore, String cognomeAutore) {
		this.idLibro = idLibro;
		this.titolo = titolo;
		this.numCopie = numCopie;
		this.nomeAutore = nomeAutore;
		this.cognomeAutore = cognomeAutore;
	}
	
	

}
